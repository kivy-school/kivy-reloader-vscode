download_family() {
    local DISPLAY_NAME="$1"
    # GitHub folder name: lowercase, spaces and hyphens removed
    local FOLDER
    FOLDER=$(echo "$DISPLAY_NAME" | tr '[:upper:]' '[:lower:]' | tr -d ' -')
    local TARGET="$FONT_DIR/$(echo "$DISPLAY_NAME" | tr ' ' '_')"
    mkdir -p "$TARGET"
    local COUNT=0
    for LICENSE in ofl apache ufl; do
        local API_URL="https://api.github.com/repos/google/fonts/contents/${LICENSE}/${FOLDER}"
        local FILE_URLS
        FILE_URLS=$(curl -fsSL "$API_URL" 2>/dev/null \
            | python3 -c "import sys,json; d=json.load(sys.stdin); [print(f['download_url']) for f in d if isinstance(d,list) and f.get('name','').endswith(('.ttf','.otf'))]" 2>/dev/null) || true
        if [ -n "$FILE_URLS" ]; then
            while IFS= read -r url; do
                local fname
                fname=$(basename "$url")
                curl -fsSL "$url" -o "$TARGET/$fname" 2>/dev/null || true
                COUNT=$((COUNT + 1))
            done <<< "$FILE_URLS"
            break
        fi
    done
    if [ "$COUNT" -eq 0 ]; then
        rmdir "$TARGET" 2>/dev/null || true
        echo "    (not found on GitHub, skipped)"
    else
        echo "    OK ($COUNT files)"
    fi
}

for FAMILY in "${FAMILIES[@]}"; do
    DISPLAY_NAME="${FAMILY//+/ }"
    echo "==> Downloading: $DISPLAY_NAME"
    download_family "$DISPLAY_NAME"
done

echo "==> Updating fontconfig cache..."
fc-cache -fv > /dev/null 2>&1
echo "==> Done."
